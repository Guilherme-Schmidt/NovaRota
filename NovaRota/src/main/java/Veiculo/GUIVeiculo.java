    public Veiculo getVeiculo() {
        Veiculo v = new Veiculo();
        v.setPlaca(JTFplaca.getText());
        v.setMarca(JTFmarca.getText());
        v.setModelo(JTFmodelo.getText());
        v.setAno(Integer.parseInt(JTFano.getText()));
        v.setCor(JTFcor.getText());
        return v;
    }

