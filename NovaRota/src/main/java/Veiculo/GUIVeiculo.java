    public void consultar() {
        try {
            DAOVeiculo objVeiculoDAO = new DAOVeiculo();
            DefaultTableModel model = (DefaultTableModel) JTBveiculo.getModel();
            ArrayList<Veiculo> lista = objVeiculoDAO.Display();
            model.setRowCount(0);
            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getPlaca(),
                    lista.get(num).getMarca(),
                    lista.get(num).getModelo(),
                    lista.get(num).getAno(),
                    lista.get(num).getCor()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }

